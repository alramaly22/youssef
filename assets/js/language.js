let selector = document.getElementById('language-selector');

// استدعاء الدالة عند تغيير اللغة
selector.addEventListener('change', updateLanguage);

// استدعاء الدالة لتعيين اللغة الافتراضية عند تحميل الصفحة
updateLanguage();

function updateLanguage() {
    let language = selector.value; // اللغة المختارة
    let selectedOption = selector.options[selector.selectedIndex];
    let flagUrl = selectedOption.getAttribute('data-flag'); // رابط العلم

    // تحديث صورة العلم في خلفية <select>
    selector.style.backgroundImage = `url(${flagUrl})`;

    let nodes = document.querySelectorAll('[data-lang]');

    // تحديث نصوص العناصر باستخدام اللغة المناسبة
    nodes.forEach(node => {
        let key = node.getAttribute('data-lang');
        if (lang[language] && lang[language][key]) {
            node.textContent = lang[language][key];
        }

        // إضافة الفئات بناءً على اللغة المختارة
        if (language === 'ar') {
            node.classList.remove('english-font');
            node.classList.add('arabic-font');
        } else {
            node.classList.remove('arabic-font');
            node.classList.add('english-font');
        }
    });

    // تحديث اتجاه النص في الجسم بناءً على اللغة
    document.body.style.direction = language === 'ar' ? 'rtl' : 'ltr';
    document.body.style.textAlign = language === 'ar' ? 'right' : 'left';

    // أقسام ذات اتجاه معين تبقى كما هي
    let sections = ['new', 'travel', 'clients', 'discount'];
    sections.forEach(id => {
        let section = document.getElementById(id);
        if (section) {
            section.style.direction = 'ltr';
            section.style.textAlign = 'left';
        }
    });

    let stepsSection = document.querySelector('.steps');
    if (stepsSection) {
        stepsSection.style.direction = 'ltr';
        stepsSection.style.textAlign = 'left';
    }

    let homeSection = document.querySelector('.home3');
    if (homeSection) {
        homeSection.style.direction = 'ltr';
        homeSection.style.textAlign = 'left';
    }

    let testimonialSection = document.querySelector('.testimonial');
    if (testimonialSection) {
        testimonialSection.style.direction = 'ltr';
        testimonialSection.style.textAlign = 'left';
    }
}